<p>Before letting you go lets take a look at one last function - The Stocks System. If you are unfamiliar with stocks then the stocks are something that is purchased and sold to gain profit. People usually buy when a company is worth little and sell their stocks when its worth a lot. If you have a friend at Hospital Entrepreneur you can buy some stocks in his hospital and then sell when they are worth a lot. That way you will earn money.</p>
