<h1>The User Interface</h1>
<p>Welcome to the User Interface tutorial. This will help you to understand the different parts of the User Interface at Hospital Entrepreneur</p>
<br />
<br />
<h2>The Overview</h2>
<p>The first thing you see when you log into Hospital Entrepreneur is the overview. At the overview you get quick information about the latest news and the current events happening to you, as well as a log of transactions executed at your Hospital</p>

<img class="border" src="/template/images/tutorial/overview/overview.png" />
<span class="img">Here you see the standard overview.</span>
<br />
<br />


<h2>User Information</h2>
<div style=" background-image: url(/template/images/tutorial/overview/user_information.png); background-position: -184px; width: 750px; height: 45px;"></div>
<p>One of the central parts of the website is the very top part. Here you will find lots of important information about your unique Hospital.</p>



<h3>From Left to Right</h3>
<ul class="text14">
	<li>Events that needs interaction</li>
	<li>The amount of money you have disposable</li>
	<li>The value of your company (e.g. how much it is worth)</li>
	<li>Your Premium Expiration date. Only shown if you have paid for a Premium membership</li>
	<li>Your Stocks Value</li>
	<li>Hospital area remaining. This is the area you have at disposal to expand your hospital</li>
	<li>Unread Mail. During your game time you may get messages from the staff or other players. They will be here</li>
	<li>Patients cured</li>
	<li>Employees Hired</li>
	<li>Pills. These are the things used to pay for premium features. You will gain these when you play the game but you can also purchase them.</li>
</ul>

<br /><br />

<h2>User Menu</h2>
<img class="border right_align" src="/template/images/tutorial/overview/user_menu.png" />
<p>The main menu is your portal to everything at the website. This is the place your mouse will hover around most of the time as you navigate throughout the website.</p>
<br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br /><br />

<h2>Chat</h2>
<p>Hospital Entrepreneneur features an in game chat which is much similar to the chat found on Facebook. If you click on the online users list you will get a list of all online users. You then click one of their names then a new window opens (In the picture that is blue). Where you can chat with them.</p>
<img class="border right_align" src="/template/images/tutorial/overview/chat.png" />
